<h1>Groupement banque assurance Francais</h1>
<p>Le Groupement Banque Assurance Français (GBAF) est une fédération représentant les 6 grands groupes français:</p>
<ul>
<li>BNP Paribas;</li>
<li>BPCE;</li>
<li>Crédit Agricole;</li>
<li>Credit Mutuel-CIC;</li>
<li>Société Générale;</li>
<li>La Banque Postale.</li>
</ul>
<p>Le GBAF est le représentant de la profession bancaire et des assureurs sur tous les axes de la réglementation financière française. Sa mission est de promouvoir l'activité bancaire à l'echelle nationale. C'est aussi un interlocuteur privilégié des pouvoirs publics.</p>
